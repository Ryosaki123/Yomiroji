window.tr = function (key, ...args) {
  const d = DICT[CUR] || DICT.ja;
  let v = key in d ? d[key] : (key in DICT.en ? DICT.en[key] : key);
  return typeof v === "function" ? v(...args) : v;
};
window.emoLabel = function (id) { return window.tr("emo_" + id); };
